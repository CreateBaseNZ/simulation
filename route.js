/* ==========================================================
MODULES
========================================================== */

const express = require("express");
const path = require("path");

/* ==========================================================
VARIABLES
========================================================== */

const router = new express.Router();

/* ==========================================================
ROUTES
========================================================== */

router.get("/", (req, res) => {
  res.sendFile("index.html", {
    root: path.join(__dirname, "./dist")
  });
  return;
});

/* ==========================================================
EXPORT
========================================================== */

module.exports = router;

/* ==========================================================
END
========================================================== */